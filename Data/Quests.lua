-- Earliest phase at which questID becomes available, or nil if not gated
-- (always available, or unknown). Used for the "Unlocks in <phase>" message.
function ns.GetQuestUnlockPhase(questID)
    return questID and ns.QuestPhases[questID] or nil
end

-- True if questID is content not yet unlocked at activePhase. Honours both the
-- QuestPhases map and any manual phase.bannedQuests override on the active phase
-- (the latter mirrors the union-style bannedItems model in Data/Phases.lua).
function ns.QuestBlockedAtPhase(questID, activePhase)
    if not (questID and activePhase) then return false end
    local unlock = ns.QuestPhases[questID]
    if unlock and unlock > activePhase then return true end
    local phase = ns.Phases[activePhase]
    if phase and phase.bannedQuests and phase.bannedQuests[questID] then return true end
    return false
end
